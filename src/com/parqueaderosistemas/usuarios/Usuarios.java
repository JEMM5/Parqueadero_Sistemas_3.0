    private void botonIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonIngresarActionPerformed
        usuario = textUsuario.getText();
        contrasena = password.getText();
        tipo = tipoUsuario.getSelectedItem().toString();
        //System.out.println("usuario :" + usuario + "\ncontrasena :" + contrasena + "\ntipo :" + tipo);
        String tipoUsuarioAlmacenado = "";
        nombreUsuarioAlmacenado = "";
        tipoUsuarioEnviar = "";
        if(usuario.trim().equals("") || contrasena.trim().equals("")){
            JOptionPane.showMessageDialog(null,"Por favor ingresa todos los campos");
            
        }else{
          UsuariosJDBC usuariosJDBC = new UsuariosJDBC();
          List<Usuario> lista = usuariosJDBC.select();
          for(Usuario usuarioObj : lista){
              int id_db = usuarioObj.getId();
              String nombre = usuarioObj.getNombre();
              String usuario_db = usuarioObj.getUsuario();
              String contrasena_db = usuarioObj.getContrasena();
              String tipoUsuario_db = usuarioObj.getTipoUsuario();
              
              if(usuario.equals(usuario_db) && contrasena.equals(contrasena_db) && tipo.equals(tipoUsuario_db)){
                  entradaSistemaAdm = true;
                  tipoUsuarioAlmacenado = tipoUsuario_db;
                  nombreUsuarioAlmacenado = nombre;
                  tipoUsuarioEnviar = tipoUsuario.getSelectedItem().toString();
              }
          }    
        }
        
        if(!entradaSistemaAdm){
            JOptionPane.showMessageDialog(null,"Datos incorrectos, por favor ingresa de nuevo");
        }else if(entradaSistemaAdm && tipoUsuarioAlmacenado.equals("Super Usuario")){
            JOptionPane.showMessageDialog(null,"Bienvenido "+ nombreUsuarioAlmacenado + " al sistema de Parqueadero Duran!");
            //tipoUsuarioEnviar = tipoUsuario.getSelectedItem().toString();
            parqueaderoBienvenida pBien = new parqueaderoBienvenida();
            pBien.setBounds(0,0,598,313);
            pBien.setVisible(true);
            pBien.setResizable(false);
            pBien.setLocationRelativeTo(null);
            this.setVisible(false);
        }else if(entradaSistemaAdm && tipoUsuarioAlmacenado.equals("Usuario")){
            JOptionPane.showMessageDialog(null,"Bienvenido " + nombreUsuarioAlmacenado + " al sistema de Registros");
            //tipoUsuarioEnviar = tipoUsuario.getSelectedItem().toString();
            ParqueaderoRegistroUsuarios pru = new ParqueaderoRegistroUsuarios();
            pru.setBounds(0,0,800,573);
            pru.setVisible(true);
            pru.setResizable(false);
            pru.setLocationRelativeTo(null);
            
            this.setVisible(false);
        }
        
    }//GEN-LAST:event_botonIngresarActionPerformed
